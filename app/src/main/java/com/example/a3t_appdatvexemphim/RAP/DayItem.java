package com.example.a3t_appdatvexemphim.RAP;

public class DayItem {
    private String thu;
    private String ngay;

    public DayItem(String thu, String ngay) {
        this.thu = thu;
        this.ngay = ngay;
    }

    public String getThu() {
        return thu;
    }
    public String getNgay() {
        return ngay;
    }


}

